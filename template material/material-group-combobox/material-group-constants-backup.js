export function searchMaterialGroupSmOptions(searchTerm) {
  if (!searchTerm) return MATERIAL_GROUP_SM_OPTIONS;
  
  const term = searchTerm.toLowerCase();
  return MATERIAL_GROUP_SM_OPTIONS.filter(
    (option) =>
      option.code.toLowerCase().includes(term) ||
      option.label.toLowerCase().includes(term)
  );
}

export function getMaterialGroupSmLabel(code) {
  const option = MATERIAL_GROUP_SM_OPTIONS.find((opt) => opt.code === code);
  return option ? `${option.code} - ${option.label}` : code;
}

