// Função para buscar unidades
export function searchMaterialUnitOptions(searchTerm) {
  if (!searchTerm) return MATERIAL_UNIT_OPTIONS;
  
  const term = searchTerm.toLowerCase();
  return MATERIAL_UNIT_OPTIONS.filter(
    (option) =>
      option.code.toLowerCase().includes(term) ||
      option.label.toLowerCase().includes(term)
  );
}

// Obter label da unidade
export function getMaterialUnitLabel(code) {
  const option = MATERIAL_UNIT_OPTIONS.find((opt) => opt.code === code);
  return option ? `${option.code} - ${option.label}` : code;
}

// Obter apenas o código
export function getMaterialUnitCode(value) {
  if (!value) return '';
  // Se o valor já é só o código, retorna
  if (!value.includes(' - ')) return value;
  // Se é "CODE - Label", extrai o código
  return value.split(' - ')[0];
}

