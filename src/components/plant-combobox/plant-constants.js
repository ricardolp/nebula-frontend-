export function searchPlantOptions(searchTerm) {
  if (!searchTerm) return PLANT_OPTIONS;
  const term = searchTerm.toLowerCase();
  return PLANT_OPTIONS.filter(
    (option) =>
      option.code.toLowerCase().includes(term) ||
      option.label.toLowerCase().includes(term)
  );
}

export function getPlantLabel(code) {
  const option = PLANT_OPTIONS.find((opt) => opt.code === code);
  return option ? `${option.code} - ${option.label}` : code;
}

export function getPlantCode(value) {
  if (!value) return '';
  if (typeof value !== 'string' || !value.includes(' - ')) return value;
  return value.split(' - ')[0];
}
